package com.github.samcarlberg.colorburst;

/**
 * 2-dimensional integer coordinates.
 */
public final class Pos {

  /**
   * The X-coordinate of this position.
   */
  public final int x;

  /**
   * The Y-coordinate of this position.
   */
  public final int y;

  public Pos(int x, int y) {
    this.x = x;
    this.y = y;
  }
}
